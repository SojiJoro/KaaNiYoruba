export function Switch({ on, onToggle }: { on: boolean; onToggle: () => void }) {
  return (
    <button
      type="button"
      role="switch"
      aria-checked={on}
      className={"switch" + (on ? " switch-on" : "")}
      onClick={onToggle}
    >
      <span className="switch-knob"></span>
    </button>
  );
}

